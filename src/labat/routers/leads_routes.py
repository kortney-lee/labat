# ── Lead Form Creation ────────────────────────────────────────────────────────

from pydantic import BaseModel, Field
from typing import List, Dict, Any


class CreateLeadFormRequest(BaseModel):
    page_id: str = Field(..., description="Facebook Page ID to attach the form to")
    name: str = Field(..., min_length=1, max_length=200)
    privacy_policy_url: str
    thank_you_url: Optional[str] = None
    product: Optional[str] = Field(None, description="Brand/product name (wihy, communitygroceries, etc.). Auto-injects qualifying questions when questions is omitted.")
    questions: Optional[List[Dict[str, Any]]] = Field(
        None,
        description="Form fields. If omitted and product is set, qualifying questions are loaded from strategy_rules.",
    )


@router.post("/forms")
async def create_lead_form(body: CreateLeadFormRequest, _=Depends(require_admin)):
    """Create a lead gen form on a Facebook Page.

    If ``questions`` is omitted and ``product`` is provided, qualifying
    questions are loaded automatically from strategy_rules presets.
    Standard contact fields (EMAIL, FIRST_NAME) are always prepended.
    """
    from src.labat.services.strategy_rules import get_lead_form_preset

    questions = body.questions
    if questions is None:
        base = [{"type": "EMAIL"}, {"type": "FIRST_NAME"}]
        if body.product:
            preset = get_lead_form_preset(body.product)
            questions = base + preset
        else:
            questions = base + [{"type": "LAST_NAME"}]

    try:
        return await leads_service.create_lead_form(
            page_id=body.page_id,
            name=body.name,
            questions=questions,
            privacy_policy_url=body.privacy_policy_url,
            thank_you_url=body.thank_you_url,
        )
    except MetaAPIError as e:
        raise HTTPException(status_code=e.status_code or 502, detail=str(e))


