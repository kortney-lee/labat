# ── Lead Sync (Meta → Firestore → Email) ──────────────────────────────────────

from src.labat.services import lead_sync_service

# Brand scope for this instance
_BRAND_SCOPE = (os.getenv("LABAT_BRAND_SCOPE", "all") or "all").strip().lower()


@router.post("/sync")
async def sync_leads(
    form_id: Optional[str] = Query(None, description="Sync a specific form; omit to sync all"),
    brand: Optional[str] = Query(None, description="Override brand detection"),
    _=Depends(require_admin),
):
    """
    Pull new leads from Facebook Lead Forms → store in Firestore → send welcome email.
    Call periodically (cron) or manually after launching lead ads.

    When LABAT_BRAND_SCOPE is set, defaults the brand to this instance's scope.
    """
    # Default brand to this instance's scope if not explicitly provided
    effective_brand = brand or (_BRAND_SCOPE if _BRAND_SCOPE != "all" else None)
    try:
        if form_id:
            result = await lead_sync_service.sync_leads_from_form(
                form_id=form_id, brand=effective_brand
            )
        else:
            result = await lead_sync_service.sync_all_forms()
        return result
    except MetaAPIError as e:
        raise HTTPException(status_code=e.status_code or 502, detail=str(e))
    except Exception as e:
        logger.error("Lead sync failed: %s", e, exc_info=True)
        raise HTTPException(status_code=500, detail=str(e))
